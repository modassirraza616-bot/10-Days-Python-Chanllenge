# VARIABLE (PYTHON IS DYNAMIC PROGRAMMING LANGUAGE SO WE DON'T USE ANY DATATYPE FOR WRITTING VARIABLE , PYTHON AUTOMATICALLY DETECT)
X=2  #INTEGER
Y=5.0 #FLOATING VALUE
Z="HELLO" #STRING
W= True #BOOLEAN
# WANT TO PRINT THE VALUE OF VARIABLE THEN USE PRINT COMMAND WITHOUT ""
print (W)
print(X)
print(Y)
print(Z) 
# or 
print(W,X,Y,Z)
# IF YOU WANT TO CHECK THE DATATYPE OF THE VARIABLE THEN USE "TYPE" KEYWORD
print(type(W))
print(type(X))
print(type(Y))
print(type(Z))



# VARIABLE NAME MUST BE STARTED WITH LETTER , UNDERSCORE , AND NOT WITH ANY SPECIAL CHARACTER OR NUMBER , ONLY ALPHANUMERIC VALUE AND CHARACTERS.
