#LOGICAL OPERATORS
'''AND {RETURN True IF BOTH STATEMENTS ARE TRUE}
OR {RETURN TRUE IF ONE OF THE STATEMENT ID True}
NOT {RETURN THE RESULT, RETURN False IF THE RESULT IS True}'''

# example 
x= 2>1  # true
y= 3>4  # false 
print("and logical operator:", x and y)   # false
print("or logical operator:", x or y)     # true 
print("not logical operator:",not (x))    #false